"""Дан список students с информацией о результатах
контрольной работы по курсу "Алгоритмы
и программирование" в 2021 году. Каждый элемент
 списка содержит ФИО студента, номер
группы и оценку за контрольную работу. Вам необходимо:
- Для каждой группы вычислить процент писавших
контрольную студентов и процент не
писавших студентов. Вывести их на экран так, чтоб
ы было понятно какие значения
выводятся.
- Реализовать быстрый поиск информации о студенте
по имени или фамилии. Должны
выводиться ФИО, номер группы и полученная
 оценка в заданном порядке через запятую,
каждый результат на отдельной строке.
Файл с заданием: task_3.1.py"""

students = [
    ["Иванов Иван Иванович", "Группа 1", 5],
    ["Петров Петр Петрович", "Группа 2", 4],
    ["Сидоров Сидор Сидорович", "Группа 1", 3],
    ["Козлов Александр Александрович", "Группа 3", 4],
    ["Смирнова Елена Викторовна", "Группа 2", 5],
    ["Зайцева Ольга Петровна", "Группа 3", 2],
]

# Вычисляем процент писавших и не писавших студентов для каждой группы
group_counts = {}
for student in students:
    group = student[1]
    if group in group_counts:
        group_counts[group] += 1
    else:
        group_counts[group] = 1

total_students = len(students)

for group, count in group_counts.items():
    percent_written = count / total_students * 100
    percent_not_written = (1 - count / total_students) * 100
    print(
        f"Группа {group}: {percent_written:.2f}% писавших, {percent_not_written:.2f}% не писавших",
    )


# Быстрый поиск информации о студенте по имени или фамилии
def search_student(name):
    for student in students:
        if name in student[0]:
            print(f"{student[0]}, {student[1]}, {student[2]}")


search_student("Иванов")
search_student("Петров")
search_student("Елена")
